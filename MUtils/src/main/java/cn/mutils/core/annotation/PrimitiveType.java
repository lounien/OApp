package cn.mutils.core.annotation;

/**
 * IOC for primitive type enum
 */
public enum PrimitiveType {

    /**
     * String
     */
    STRING,

    /**
     * Integer of string
     */
    STRING_INT,

    /**
     * Long of string
     */
    STRING_LONG,

    /**
     * Double of string
     */
    STRING_DOUBLE,

    /**
     * Boolean of string
     */
    STRING_BOOL,

    /**
     * Integer
     */
    INT,

    /**
     * Long
     */
    LONG,

    /**
     * Double
     */
    DOUBLE,

    /**
     * Boolean
     */
    BOOL

}
